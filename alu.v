// קובץ: alu.v
// תיאור: יחידה אריתמטית-לוגית (ALU) בת 4 סיביות
// תומכת ב-8 פעולות שנבחרות דרך opcode בן 3 סיביות

module alu(
    input  [3:0] A,          // מספר קלט ראשון (4 סיביות)
    input  [3:0] B,          // מספר קלט שני  (4 סיביות)
    input  [2:0] opcode,     // קוד הפעולה: 000 עד 111
    output reg [3:0] result, // תוצאת הפעולה (4 סיביות)
    output zero,             // דגל: דולק כאשר התוצאה שווה לאפס
    output carry             // דגל: דולק אם יצאה סיבית חמישית (carry)
);

// משתנה עזר בן 5 סיביות — הסיבית ה-5 תהפוך לדגל ה-carry
reg [4:0] temp;

// --------- טבלת קודי הפעולות ---------
// opcode 000 = חיבור        (ADD)
// opcode 001 = חיסור        (SUB)
// opcode 010 = AND סיביות   (AND)
// opcode 011 = OR סיביות    (OR)
// opcode 100 = XOR סיביות   (XOR)
// opcode 101 = NOT של A     (NOT)
// opcode 110 = הזזה שמאלה   (SHL)
// opcode 111 = הזזה ימינה   (SHR)
// --------------------------------------

// דגל האפס — נדלק אוטומטית כשהתוצאה היא 0000
assign zero  = (result == 4'b0000);

// דגל ה-carry — הסיבית החמישית שנשמרה ב-temp
assign carry = temp[4];

// הבלוק הראשי — מחשב מחדש בכל פעם שהקלט משתנה
always @(*) begin

    // איפוס ברירת מחדל בתחילת כל חישוב
    temp   = 5'b00000;
    result = 4'b0000;

    case (opcode)

        // חיבור: מוסיפים B ל-A, הסיבית החמישית = carry
        3'b000: begin
            temp   = {1'b0, A} + {1'b0, B};
            result = temp[3:0];
        end

        // חיסור: מחסירים B מ-A (אם B גדול מ-A, carry=1 = "השאלה")
        3'b001: begin
            temp   = {1'b0, A} - {1'b0, B};
            result = temp[3:0];
        end

        // AND: כל סיבית דולקת רק אם שתי הסיביות המקבילות דולקות
        3'b010: begin
            result = A & B;
        end

        // OR: כל סיבית דולקת אם לפחות אחת מהסיביות המקבילות דולקת
        3'b011: begin
            result = A | B;
        end

        // XOR: כל סיבית דולקת רק אם הסיביות המקבילות שונות זו מזו
        3'b100: begin
            result = A ^ B;
        end

        // NOT: מבצע היפוך על כל סיביות A בלבד (B לא בשימוש כאן)
        3'b101: begin
            result = ~A;
        end

        // הזזה שמאלה: כל סיבית זזת מקום אחד שמאלה, 0 נכנס מימין
        // דוגמה: 0011 הופך ל-0110 (שקול לכפל ב-2)
        3'b110: begin
            result = A << 1;
        end

        // הזזה ימינה: כל סיבית זזת מקום אחד ימינה, 0 נכנס משמאל
        // דוגמה: 0110 הופך ל-0011 (שקול לחלוקה ב-2)
        3'b111: begin
            result = A >> 1;
        end

        // מקרה שלא אמור לקרות — מאפסים הכל לבטיחות
        default: begin
            result = 4'b0000;
        end

    endcase
end

endmodule
